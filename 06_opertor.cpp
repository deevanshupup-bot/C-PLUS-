#include<iostream>
using namespace std;
int main (){
    int a, b;
    cout<<"Enter the first number"<<endl;
    cin>>a;

    cout<<"Enter the second number"<<endl;
    cin>>b;

    cout << "a+b is " << a + b << "\n";  // operator
    cout << "a-b is " << a - b << "\n";  // operator
    cout << "a/b is " << a / b << "\n";  // operator
    cout << "a*b is " << a * b << "\n";  // operator

    return 0;
}